package kr.ac.kookmin.shape;

    public class Point {
	
    private int x ,y ;
	
	public Point(int x, int y){
		this.x = x;
		this.y = y;
	}
	
	public int getX(){
		return x;
	
	}
	
	public void setX(int X){
		x = X;
		
	}
	
	public int getY(){
		return y;
	}
	
	public void setY(int Y){
		y = Y;
	}
	
	
	

	}
